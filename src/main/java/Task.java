package main.java;

public class Task {
    private String name;
    private boolean isDone;

    public Task(String name) {
        this.name = name;
        this.isDone = false;
    }

    public void mark() {
        this.isDone = true;
    }

    public void unmark() {
        this.isDone = false;
    }

    @Override
    public String toString() {
        String cross = this.isDone ? "X" : " ";
        return String.format("[%s] %s", cross, this.name);
    }
}
